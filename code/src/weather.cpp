// Get color565 directly from 24-bit RGB value
// TODO - replace arrays with color565 values!
uint16_t color565(uint32_t rgb) {
  return (((rgb>>16) & 0xF8) << 8) |
    (((rgb>>8) & 0xFC) << 3) |
    ((rgb & 0xFF) >> 3);
};

